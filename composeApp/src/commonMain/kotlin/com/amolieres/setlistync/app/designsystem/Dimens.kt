package com.amolieres.setlistync.app.designsystem

import androidx.compose.ui.unit.dp

object AppDimens {
    // ── Spacing ───────────────────────────────────────────────────────────────
    /** 4dp — gaps in chip FlowRow, small list item spacing */
    val SpacingXxs = 4.dp
    /** 6dp — vertical gap between info rows */
    val SpacingXs = 6.dp
    /** 8dp — standard small spacing (between siblings, icon+text) */
    val SpacingS = 8.dp
    /** 12dp — section vertical padding, dialog inner spacers */
    val SpacingM = 12.dp
    /** 16dp — main content horizontal padding, form item spacing */
    val SpacingL = 16.dp
    /** 24dp — full-screen form padding (auth screen) */
    val SpacingXl = 24.dp

    // ── Icon sizes ────────────────────────────────────────────────────────────
    /** 16dp — trailing icon inside chips */
    val IconSizeSmall = 16.dp
    /** 18dp — icons in detail info rows */
    val IconSizeMedium = 18.dp

    // ── Component sizes ───────────────────────────────────────────────────────
    /** 20dp — CircularProgressIndicator inside a Button */
    val ButtonLoaderSize = 20.dp
    /** 2dp — stroke width of the in-button loader */
    val ButtonLoaderStrokeWidth = 2.dp

    // ── Layout ────────────────────────────────────────────────────────────────
    /** 88dp — LazyColumn bottom padding to clear a FAB */
    val FabSpacing = 88.dp
}
